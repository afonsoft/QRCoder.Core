using System;

namespace QRCoder.Core.Abstractions
{
    using QRCoder.Core.Models;

    /// <summary>
    /// AbstractQRCode
    /// </summary>
    public abstract class AbstractQRCode : IDisposable
    {
        /// <summary>
        /// QRCodeData
        /// </summary>
        protected QRCodeData QrCodeData { get; set; }

        /// <summary>
        /// AbstractQRCode
        /// </summary>
        protected AbstractQRCode()
        {
           

            //Role: csharpsquid:S6444
            //https://sonarcloud.io/organizations/afonsoft/rules?open=csharpsquid%3AS6444&rule_key=csharpsquid%3AS6444&tab=how_to_fix
            AppDomain.CurrentDomain.SetData("REGEX_DEFAULT_MATCH_TIMEOUT", TimeSpan.FromMilliseconds(500));
        }

        /// <summary>
        /// AbstractQRCode
        /// </summary>
        /// <param name="data"></param>
        protected AbstractQRCode(QRCodeData data) : this()
        {
            this.QrCodeData = data;
        }

        /// <summary>
        /// Set a QRCodeData object that will be used to generate QR code. Used in COM Objects connections
        /// </summary>
        /// <param name="data">Need a QRCodeData object generated by QRCodeGenerator.CreateQrCode()</param>
        public virtual void SetQRCodeData(QRCodeData data)
        {
            this.QrCodeData = data;
        }

        /// <summary>
        /// Releases all resources used by this instance.
        /// </summary>
        public void Dispose()
        {
            Dispose(true);
            GC.SuppressFinalize(this);
        }

        /// <summary>
        /// Releases the unmanaged resources and optionally releases the managed resources.
        /// </summary>
        /// <param name="disposing">true to release both managed and unmanaged resources; false to release only unmanaged resources.</param>
        protected virtual void Dispose(bool disposing)
        {
            if (disposing)
                this.QrCodeData?.Dispose();
            this.QrCodeData = null;
        }

        /// <summary>
        /// Finalizer.
        /// </summary>
        ~AbstractQRCode()
        {
            Dispose(false);
        }
    }
}